class Cook {
  int id;
  String cookName;
  String cookCategory;
  String duration;
  String person;
  String content;
  String recipe;
  String picture;

  Cook(int id, String cookName, String cookCategory, String duration, String person, String content, String recipe, String picture){
    this.id = id;
    this.cookName = cookName;
    this.cookCategory = cookCategory;
    this.duration = duration;
    this.person = person;
    this.content = content;
    this.recipe = recipe;
    this.picture = picture;
  }

}